package com.company.smart;

public class SmartPhone {

    //1. atributos
    String marca;
    String modelo;
    String sw;
    String operador;
    double precio;
    int ram;

    //2. constructores
    public SmartPhone() {

    }

    public SmartPhone(String marca, String modelo, String sw, String operador, double precio, int ram) {
        this.marca = marca;
        this.modelo = modelo;
        this.sw = sw;
        this.operador = operador;
        this.precio = precio;
        this.ram = ram;

    }




}

